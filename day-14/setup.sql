-- Step 1: Drop ALL existing tables
DO $$ DECLARE r RECORD;
BEGIN
    FOR r IN (SELECT tablename FROM pg_tables WHERE schemaname = 'public') LOOP
        EXECUTE 'DROP TABLE IF EXISTS public.' || quote_ident(r.tablename) || ' CASCADE';
    END LOOP;
END $$;

-- =============================================================================
-- Step 2: Create fleet_vehicles table
-- =============================================================================

CREATE TABLE fleet_vehicles (
vehicle_id            SERIAL       PRIMARY KEY,
registration          VARCHAR(10)  NOT NULL UNIQUE,
vehicle_type          VARCHAR(20)  NOT NULL
                  CHECK (vehicle_type IN ('van', 'truck', 'lorry')),
model                 VARCHAR(50)  NOT NULL,
year_manufactured     INTEGER      NOT NULL CHECK (year_manufactured >= 2015),
mileage               INTEGER      NOT NULL CHECK (mileage >= 0),
last_maintenance_date DATE,
depot_name            VARCHAR(50)  NOT NULL,
assigned_route        VARCHAR(20)  NOT NULL
                  CHECK (assigned_route IN ('urban', 'suburban', 'highway', 'mixed'))
);

